#include "libft.h"

long	ft_atoi(const char *str)
{
	long	num;
	int		neg;
	int		temp;

	num = 0;
	neg = 1;
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if (*str == 43 || *str == 45)
	{
		if (*str == 45)
			neg *= -1;
		str++;
	}
	while (*str >= 48 && *str <= 57)
	{
		temp = *str - '0';
		num = num * 10 + temp;
		str++;
	}
	return (num * neg);
}
